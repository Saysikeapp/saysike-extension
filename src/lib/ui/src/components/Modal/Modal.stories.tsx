const meta: Meta<typeof Modal.Overlay> = {
  component: Modal.Overlay,
  title: "Modal",
  argTypes: {},
};
export default meta;

type Story = StoryObj<typeof Modal.Overlay>;

const defaultArgs = {};

const StoryContainer = ({
  children,
}: {
  children: React.ReactNode;
}): React.ReactElement => {
  const [showModal, setShowModal] = useState<boolean>(false);
  const handleClose = (): void => {
    setShowModal(false);
  };
  return (
    <div className="w-full">
      <div className="flex justify-evenly">
        <Button
          onClick={() => {
            setShowModal(true);
          }}
        >
          Click to Open Modal
        </Button>

        <Modal.Overlay showModal={showModal}>
          <Modal.Panel onClose={handleClose} closeOnClickOutside={false}>
            <Modal.Title>Test title</Modal.Title>
            <Modal.Body> {children} </Modal.Body>
            <Modal.Footer>
              <div className="float-right">
                <Button
                  onClick={() => {
                    setShowModal(false);
                  }}
                  variant="ghost"
                >
                  Cancel
                </Button>
                <Button onClick={() => {}}>Confirm</Button>
              </div>
            </Modal.Footer>
          </Modal.Panel>
        </Modal.Overlay>
      </div>
    </div>
  );
};

interface StoryContainerProps {
  children: React.ReactNode;
}

